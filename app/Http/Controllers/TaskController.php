     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store($id)
    {
        $projectID = Project::find($id);

        $pid = $projectID->id;

        $task = new Task;

        $task->taskname = request('taskname');
        $task->project_id = $pid;

        $task->save();
        
        return redirect('/');
        
     
    }

